function startTimer() {
  clearInterval(timer);
  timeLeft = 30;
  timerText.innerText = timeLeft;
  timerBar.style.strokeDashoffset = 0;
  timerBar.style.stroke = "#00ffcc";

  timer = setInterval(() => {
    timeLeft--;
    timerText.innerText = timeLeft;

    // تأثير لوني عند الاقتراب من الانتهاء
    if (timeLeft <= 3) {
      timerBar.style.stroke = "#e74c3c";
    } else if (timeLeft <= 10) {
      timerBar.style.stroke = "#f39c12";
    } else {
      timerBar.style.stroke = "#00ffcc";
    }

    const dashOffset = 251.33 * ((30 - timeLeft) / 30);
    timerBar.style.strokeDashoffset = dashOffset;

    if (timeLeft <= 0) {
      clearInterval(timer);
      autoNext();
    }
  }, 1000);
}

function autoNext() {
  const buttons = document.querySelectorAll("#answers button");
  buttons.forEach(b => b.disabled = true);
  gsap.to("#quiz-box", {
    opacity: 0,
    y: -50,
    duration: 0.4,
    onComplete: () => {
      current++;
      if (current < questions.length) {
        loadQuestion();
        startTimer();
      } else {
        showResult();
      }
    }
  });
}

// دالة اختيار المادة
function startQuiz(subject) {
  questions = quizData[subject];
  currentSubject = subject;
  current = 0;
  score = 0;
  wrongCount = 0;

  // إضافة تأثير الخروج لشاشة اختيار المادة
  gsap.to("#subject-screen", {
    opacity: 0,
    scale: 0.95,
    y: -30,
    duration: 0.5,
    ease: "power2.in",
    onComplete: () => {
      document.getElementById("subject-screen").classList.add("hidden");
      document.getElementById("quiz-box").classList.remove("hidden");
      nextBtn.classList.remove("hidden");

      // تأثير الدخول لشاشة الكويز
      gsap.fromTo("#quiz-box",
        { opacity: 0, scale: 0.8, y: 50 },
        { 
          opacity: 1, 
          scale: 1, 
          y: 0, 
          duration: 0.6, 
          ease: "back.out(1.7)"
        }
      );

      // إطلاق حفلة احتفالية عند البدء
      confetti({
        particleCount: 150,
        spread: 100,
        origin: { y: 0.5 }
      });

      loadQuestion();
      startTimer();
    }
  });
}

// تحميل السؤال الحالي
function loadQuestion() {
  const q = questions[current];
  questionEl.innerText = q.question;
  answersEl.innerHTML = "";

  progressBar.style.width = ((current / questions.length) * 100) + "%";

  gsap.fromTo("#quiz-box",
    { opacity: 0, y: 50 },
    { opacity: 1, y: 0, duration: 0.6 }
  );

  q.answers.forEach((ans, index) => {
    const btn = document.createElement("button");
    btn.innerText = ans;

    btn.onclick = () => handleAnswer(btn, index, q.correct);

    answersEl.appendChild(btn);

    gsap.fromTo(btn,
      { opacity: 0, y: 20 },
      { opacity: 1, y: 0, duration: 0.4, delay: index * 0.1 }
    );
  });
}

// التعامل مع الإجابة
function handleAnswer(btn, index, correctIndex) {
  const buttons = document.querySelectorAll("#answers button");
  const q = questions[current];
  buttons.forEach(b => b.disabled = true);

  if (index === correctIndex) {
    btn.classList.add("correct");
    score++;
    
    // تشغيل صوت الإجابة الخاطئة للإجابة الصحيحة
    wrongSound.currentTime = 0;
    wrongSound.play().catch(err => console.log("خطأ في تشغيل الصوت:", err));

    confetti({
      particleCount: 100,
      spread: 70,
      origin: { y: 0.6 }
    });

    gsap.to(btn, { scale: 1.2, duration: 0.3, yoyo: true, repeat: 1 });

    // عرض التعليل في الحاوية المنفصلة
    setTimeout(() => {
      if (q.explanation) {
        explanationPanel.classList.remove("hidden");
        explanationContent.innerHTML = q.explanation;
        
        // إعادة تعيين الزر
        explanationToggleBtn.innerHTML = "📚 إخفاء التعليل";
        explanationContent.classList.remove("hidden");
      }
    }, 500);
  } else {
    btn.classList.add("wrong");
    wrongCount++;
    
    // تشغيل صوت الإجابة الصحيحة للإجابة الخاطئة
    correctSound.currentTime = 0;
    correctSound.play().catch(err => console.log("خطأ في تشغيل الصوت:", err));
    
    gsap.to(btn, { x: -10, duration: 0.1, repeat: 5, yoyo: true });
  }
}

// دالة التبديل بين إظهار وإخفاء التعليل
explanationToggleBtn.addEventListener("click", () => {
  explanationContent.classList.toggle("hidden");
  if (explanationContent.classList.contains("hidden")) {
    explanationToggleBtn.innerHTML = "📚 اظهار التعليل";
  } else {
    explanationToggleBtn.innerHTML = "📚 إخفاء التعليل";
  }
});

// زر التالي
nextBtn.onclick = () => {
  clearInterval(timer);
  // إخفاء التعليل قبل الانتقال للسؤال التالي
  explanationPanel.classList.add("hidden");
  explanationContent.classList.add("hidden");
  explanationToggleBtn.innerHTML = "📚 اظهار التعليل";
  
  gsap.to("#quiz-box", {
    opacity: 0,
    y: -50,
    duration: 0.4,
    onComplete: () => {
      current++;
      if (current < questions.length) {
        loadQuestion();
        startTimer();
      } else {
        showResult();
      }
    }
  });
};

// عرض النتيجة
function showResult() {
  clearInterval(timer);
  document.getElementById("quiz-box").classList.add("hidden");
  nextBtn.classList.add("hidden");

  resultEl.classList.remove("hidden");
  const percentage = Math.round((score / questions.length) * 100);
  resultEl.innerHTML = `
    <div class="result-content">
      <div class="result-score">🎉 ${score} / ${questions.length}</div>
      <div class="result-percent">${percentage}%</div>
      <div class="result-message">
        ${percentage === 100 ? "🔥 عبقري!" : percentage >= 80 ? "👏 ممتاز!" : percentage >= 60 ? "😊 جيد!" : "💪 حاول مرة أخرى"}
      </div>
      <div class="answers-summary">
        <button class="summary-btn correct-btn">✅ الإجابات الصحيحة: ${score}</button>
        <button class="summary-btn wrong-btn">❌ الإجابات الخاطئة: ${wrongCount}</button>
      </div>
    </div>
  `;

  gsap.fromTo("#result",
    { scale: 0, opacity: 0 },
    { scale: 1, opacity: 1, duration: 0.8, ease: "back.out(1.7)" }
  );

  // زر إعادة المحاولة
  const retryBtn = document.createElement("button");
  retryBtn.classList.add("result-btn", "retry-btn");
  retryBtn.innerText = "🔄 إعادة المحاولة";
  retryBtn.onclick = () => {
    document.getElementById("result").classList.add("hidden");
    document.getElementById("quiz-box").classList.remove("hidden");
    nextBtn.classList.remove("hidden");
    startQuiz(currentSubject);
  };
  resultEl.appendChild(retryBtn);

  // زر اختيار مادة أخرى
  const changeSubjectBtn = document.createElement("button");
  changeSubjectBtn.classList.add("result-btn", "change-subject-btn");
  changeSubjectBtn.innerText = "📚 اختر مادة أخرى";
  changeSubjectBtn.onclick = () => {
    // تأثير الخروج من النتيجة
    gsap.to("#result", {
      opacity: 0,
      scale: 0.8,
      y: -30,
      duration: 0.4,
      ease: "power2.in",
      onComplete: () => {
        document.getElementById("result").classList.add("hidden");
        document.getElementById("subject-screen").classList.remove("hidden");
        
        // تأثير الدخول لشاشة اختيار المادة
        gsap.fromTo("#subject-screen",
          { opacity: 0, scale: 0.95 },
          { opacity: 1, scale: 1, duration: 0.6, ease: "back.out(1.7)" }
        );
      }
    });
  };
  resultEl.appendChild(changeSubjectBtn);
}
